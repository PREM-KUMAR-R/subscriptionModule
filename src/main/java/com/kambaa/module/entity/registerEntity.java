package com.kambaa.module.entity;

public class registerEntity {
	private String name;
	private long moblieNumber;
	private int mobilePin;
	private String flatNumber;
	private String street;
	private String villageOrTown;
	private String district;
	private long pincode;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public long getMoblieNumber() {
		return moblieNumber;
	}
	public void setMoblieNumber(long moblieNumber) {
		this.moblieNumber = moblieNumber;
	}
	public int getMobilePin() {
		return mobilePin;
	}
	public void setMobilePin(int mobilePin) {
		this.mobilePin = mobilePin;
	}
	public String getFlatNumber() {
		return flatNumber;
	}
	public void setFlatNumber(String flatNumber) {
		this.flatNumber = flatNumber;
	}
	public String getStreet() {
		return street;
	}
	public void setStreet(String street) {
		this.street = street;
	}
	public String getVillageOrTown() {
		return villageOrTown;
	}
	public void setVillageOrTown(String villageOrTown) {
		this.villageOrTown = villageOrTown;
	}
	public String getDistrict() {
		return district;
	}
	public void setDistrict(String district) {
		this.district = district;
	}
	public long getPincode() {
		return pincode;
	}
	public void setPincode(long pincode) {
		this.pincode = pincode;
	}
}
